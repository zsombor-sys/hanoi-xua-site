const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) entry.target.classList.add('in');
  });
}, { threshold: 0.14 });

document.querySelectorAll('.reveal').forEach((el) => observer.observe(el));

const tiles = document.querySelectorAll('.menu-tile');
const lightbox = document.getElementById('lightbox');
const lightboxImage = document.getElementById('lightboxImage');
const lightboxTitle = document.getElementById('lightboxTitle');
const lightboxClose = document.getElementById('lightboxClose');
const langButtons = document.querySelectorAll('.lang-btn');
const descriptionMeta = document.querySelector('meta[name="description"]');

let currentLanguage = 'hu';
let activeTile = null;

function getNestedValue(source, key) {
  return key.split('.').reduce((value, segment) => (value ? value[segment] : undefined), source);
}

function setLightboxContent() {
  if (!activeTile) return;
  const titleKey = activeTile.dataset.menuKey;
  const translatedTitle = getNestedValue(translations[currentLanguage], titleKey);
  lightboxImage.alt = translatedTitle;
  lightboxTitle.textContent = translatedTitle;
}

function switchLanguage(lang) {
  if (!translations[lang]) return;

  currentLanguage = lang;

  document.documentElement.lang = lang;
  document.title = translations[lang].meta.title;
  if (descriptionMeta) descriptionMeta.setAttribute('content', translations[lang].meta.description);

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    const value = getNestedValue(translations[lang], key);
    if (typeof value === 'string') element.textContent = value;
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach((element) => {
    const key = element.dataset.i18nAriaLabel;
    const value = getNestedValue(translations[lang], key);
    if (typeof value === 'string') element.setAttribute('aria-label', value);
  });

  langButtons.forEach((button) => {
    button.classList.toggle('active', button.dataset.lang === lang);
    button.setAttribute('aria-pressed', String(button.dataset.lang === lang));
  });

  setLightboxContent();
  window.localStorage.setItem('preferredLanguage', lang);
}

tiles.forEach((tile) => {
  tile.addEventListener('click', () => {
    activeTile = tile;
    lightboxImage.src = tile.dataset.img;
    setLightboxContent();
    lightbox.classList.add('open');
    lightbox.setAttribute('aria-hidden', 'false');
  });
});

function closeLightbox() {
  lightbox.classList.remove('open');
  lightbox.setAttribute('aria-hidden', 'true');
}

langButtons.forEach((button) => {
  button.addEventListener('click', () => switchLanguage(button.dataset.lang));
});

lightboxClose.addEventListener('click', closeLightbox);
lightbox.addEventListener('click', (event) => {
  if (event.target === lightbox) closeLightbox();
});
document.addEventListener('keydown', (event) => {
  if (event.key === 'Escape') closeLightbox();
});

const storedLanguage = window.localStorage.getItem('preferredLanguage');
switchLanguage(storedLanguage && translations[storedLanguage] ? storedLanguage : 'hu');
